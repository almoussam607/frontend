// =========== ASSIGNMENT OPERATORS ==========
let a = 8;
let b = 2;

a += b; // a = a + b ------ a = 8 + 2; --- 10
a -= b; // a = a - b ------ a = 10 - 2; --- 8
a *= b; // a = a * b ------ a = 8 * 2; --- 16
a /= b; // a = a / b ------ a = 16 / 2 --- 8 
a **= b; // a = a pow b --- 64
a %= 5; // a = a % 5 --- 4
console.log(a);
