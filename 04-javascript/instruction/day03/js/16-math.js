// Math object

// Math.PI
const pi = Math.PI;
console.log('PI:', pi);

// Math.round()
const numToRound = 3.5;
const roundedNum = Math.round(numToRound);
console.log('Rounded Number:', roundedNum);

// Math.ceil()
const numToCeil = 3.1;
const ceiledNum = Math.ceil(numToCeil);
console.log('Ceiled Number:', ceiledNum);

// Math.floor()
const numToFloor = 3.9;
const flooredNum = Math.floor(numToFloor);
console.log('Floored Number:', flooredNum);

// Math.abs()
const numToAbs = -4;
const absNum = Math.abs(numToAbs);
console.log('Absolute Number:', absNum);

// Math.pow()
const base = 2;
const exponent = 3;
const result = Math.pow(base, exponent);
console.log('Power:', result);

// Math.sqrt()
const numToSqrt = 16;
const sqrtNum = Math.sqrt(numToSqrt);
console.log('Square Root:', sqrtNum);

